package com.hilel;

public class MatchOperations {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        int c = a + b;


        System.out.println("a + b = " + c);
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

        System.out.println("a++ = " + a++);
        System.out.println(a);

        System.out.println("++a = " + ++a);
        System.out.println(a);

        System.out.println("a-- = " + a--);
        System.out.println(a);

        System.out.println("--a = " + --a);
        System.out.println(a);











    }
}
